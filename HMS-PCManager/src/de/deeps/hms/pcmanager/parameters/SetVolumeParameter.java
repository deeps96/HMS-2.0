package de.deeps.hms.pcmanager.parameters;

/**
 * @author Deeps
 */

public class SetVolumeParameter {

	private int volume;
	private String device;

	public int getVolume() {
		return volume;
	}

	public void setVolume(int volume) {
		this.volume = volume;
	}

	public String getDevice() {
		return device;
	}

	public void setDevice(String device) {
		this.device = device;
	}

}
