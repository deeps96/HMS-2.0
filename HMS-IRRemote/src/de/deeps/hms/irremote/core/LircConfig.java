public class LircConfig {

	private int lircPort;
	private String lircAddress;

	public int getLircPort() {
		return lircPort;
	}

	public void setLircPort(int lircPort) {
		this.lircPort = lircPort;
	}

	public String getLircAddress() {
		return lircAddress;
	}

	public void setLircAddress(String lircAddress) {
		this.lircAddress = lircAddress;
	}

}
